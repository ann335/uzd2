function translate(lang) {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const keys = el.dataset.i18n.split(".");
    let text = translations[lang];
    keys.forEach(k => {
      if(text) text = text[k];
    });
    if (el.id === "visitor-counter") {
      const count = localStorage.getItem("visitCount") || 1;
      if (text) {
        el.textContent = text.replace("{{count}}", count);
      }
    } else {
      if (text) el.textContent = text;
    }
  });
}
function showSection(id) {
  document.querySelectorAll(".section").forEach(section => {
    section.classList.remove("active");
  });
  const section = document.getElementById(id);
  if (section) {
    section.classList.add("active");
    localStorage.setItem("activeSection", id);
  }
}
function incrementVisitCount() {
  let count = parseInt(localStorage.getItem("visitCount") || "0", 10);
  count++;
  localStorage.setItem("visitCount", count.toString());
}
function setupThemeToggle() {
  const themeButton = document.getElementById("toggle-theme");
  if (!themeButton) return;

  const savedTheme = localStorage.getItem("theme");
  if (savedTheme === "dark") {
    document.body.classList.add("dark-theme");
    themeButton.textContent = "☀️";
  } else {
    themeButton.textContent = "🌙";
  }
  themeButton.addEventListener("click", () => {
    if (document.body.classList.toggle("dark-theme")) {
      localStorage.setItem("theme", "dark");
      themeButton.textContent = "☀️";
    } else {
      localStorage.setItem("theme", "light");
      themeButton.textContent = "🌙";
    }
  });
}
function setupLanguageSwitcher() {
  const langButtons = document.querySelectorAll("[data-lang]");
  langButtons.forEach(button => {
    button.addEventListener("click", () => {
      const lang = button.dataset.lang;
      localStorage.setItem("language", lang);
      translate(lang);
    });
  });
}
function init() {
  incrementVisitCount();
  const savedLang = localStorage.getItem("language") || "lv";
  translate(savedLang);
  const savedSection = localStorage.getItem("activeSection") || "home";
  showSection(savedSection);
  setupThemeToggle();
  setupLanguageSwitcher();
  document.querySelectorAll(".nav-link").forEach(link => {
    link.addEventListener("click", e => {
      e.preventDefault();
      const target = link.getAttribute("href").substring(1);
      showSection(target);
    });
  });
  const yearEl = document.getElementById("year");
  if (yearEl) {
    yearEl.textContent = new Date().getFullYear();
  }
}
document.addEventListener("DOMContentLoaded", init);
